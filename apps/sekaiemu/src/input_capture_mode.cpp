int RunInputCaptureMode(const LaunchRequest& request, std::string& error) {
  if (request.input_capture_path.empty()) {
    error = "Input capture path is empty.";
    return 1;
  }
  if (SDL_WasInit(SDL_INIT_VIDEO | SDL_INIT_GAMECONTROLLER | SDL_INIT_JOYSTICK) == 0) {
    if (SDL_Init(SDL_INIT_VIDEO | SDL_INIT_GAMECONTROLLER | SDL_INIT_JOYSTICK) != 0) {
      error = SDL_GetError();
      return 1;
    }
  }

  SDL_Window* window = SDL_CreateWindow(
      "Sekaiemu Controller Capture",
      SDL_WINDOWPOS_CENTERED,
      SDL_WINDOWPOS_CENTERED,
      680,
      160,
      SDL_WINDOW_SHOWN | SDL_WINDOW_ALLOW_HIGHDPI);
  if (!window) {
    error = SDL_GetError();
    return 1;
  }

  SDL_GameController* controller = nullptr;
  for (int index = 0; index < SDL_NumJoysticks(); ++index) {
    if (SDL_IsGameController(index)) {
      controller = SDL_GameControllerOpen(index);
      if (controller) break;
    }
  }

  const auto controls = ControlsForProfile(request.input_capture_profile);
  std::vector<std::string> tokens(controls.size());
  std::size_t current = 0;
  SetInstruction(window, controls, current);

  bool running = true;
  bool completed = false;
  while (running) {
    SDL_Event event;
    while (SDL_WaitEvent(&event)) {
      if (event.type == SDL_QUIT) {
        running = false;
        break;
      }
      if (event.type == SDL_KEYDOWN && event.key.keysym.sym == SDLK_ESCAPE) {
        error = "Input capture cancelled.";
        running = false;
        break;
      }
      std::string token;
      if (event.type == SDL_CONTROLLERBUTTONDOWN) {
        token = ControllerButtonToken(event.cbutton.button);
      } else if (event.type == SDL_CONTROLLERAXISMOTION) {
        token = ControllerAxisToken(event.caxis.axis, event.caxis.value);
      }
      if (token.empty()) continue;
      tokens[current] = token;
      current += 1;
      if (current >= controls.size()) {
        completed = true;
        running = false;
        break;
      }
      SetInstruction(window, controls, current);
    }
  }

  if (controller) SDL_GameControllerClose(controller);
  SDL_DestroyWindow(window);
  if (!completed) return 2;
  if (!WriteCaptureConfig(request.input_capture_path, controls, tokens, error)) return 1;
  SDL_ShowSimpleMessageBox(SDL_MESSAGEBOX_INFORMATION, "Sekaiemu", "Controller capture complete.", nullptr);
  return 0;
}

