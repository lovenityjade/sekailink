import random


def randomize_d8_ice_puzzle(room_data: list[bytearray]) -> None:
    room = room_data[0x686]

    ## Empty the room
    # Ice blocks
    room[0x36] = 0x8c
    room[0x54] = 0x8c
    room[0x67] = 0x8c

    # Blocks
    room[0x29] = 0x8c
    room[0x33] = 0x8c
    room[0x3c] = 0x8c
    room[0x53] = 0x8c
    room[0x87] = 0x8c

    presets = [
        {
            # Only difficulty is the first move
            "ice": [0x38, 0x39, 0x3a, 0x47, 0x49, 0x4b, 0x57, 0x5b, 0x67, 0x69, 0x6b, 0x78, 0x79, 0x7a],
            "blocks": []
        },
        {
            "ice": [0x69, 0x89, 0x5d],
            "blocks": [0x96, 0x56, 0x38]
        },
        {
            "ice": [0x49, 0x55, 0x77],
            "blocks": [0x3a, 0x46, 0x63, 0x96, 0x97]
        },
        {
            "ice": [0x2a, 0x54, 0x55],
            "blocks": [0x1a, 0x1b, 0x1c, 0x2c, 0x3c, 0x1c, 0x78, 0x79]
        },
        {
            "ice": [0x25, 0x35, 0x36, 0x37, 0x38, 0x39, 0x3a, 0x3b, 0x3c, 0x75, 0x76, 0x77, 0x78, 0x79, 0x7a, 0x7b, 0x7c],
            "blocks": []
        },
        {
            "ice": [0x39, 0x78, 0x7b],
            "blocks": [0x28, 0x3c]
        },
        {
            "ice": [0x37, 0x39, 0x3b, 0x77, 0x79, 0x7a],
            "blocks": []
        },
    ]

    config = random.choice(presets)
    for ice in config["ice"]:
        room[ice] = 0x2f
    for block in config["blocks"]:
        room[block] = 0x1d