@dataclass
class RAC3LOCATIONDATA:
    ID: int = 0
    REGION: str = RAC3REGION.GALAXY
    CHECK_ADDRESS: list[RAC3ADDRESSDATA] = None
    AP_CODE: int = None
    TAGS: set[str] = None

    def __init__(self,
                 idx: int,
                 region: str = RAC3REGION.GALAXY,
                 check: list[RAC3ADDRESSDATA] = (),
                 tags: set[str] = None):
        self.ID = idx
        self.REGION = region
        self.CHECK_ADDRESS = check
        self.AP_CODE = idx + 51000000
        self.TAGS = tags

    @staticmethod
    def construct(location_name: str):
        planet_name: str = LOCATION_NAME_TO_REGION[location_name]
        planet_id: int = RAC3_REGION_DATA_TABLE[planet_name].ID
        loc_id: int = LOCATION_NAME_TO_ID[location_name]
        tags: set[str] = set(LOCATION_NAME_TO_TAG[location_name])
        if planet_id:
            section_id: int = LOCATION_NAME_TO_SECTION[location_name]
            UT_MAPPING[f"{planet_id}/{loc_id}"] = loc_id + 51000000
            UT_MAPPING[f"{section_id}/{loc_id}"] = loc_id + 51000000
            tags.union({planet_name})
        else:
            match planet_name:
                case RAC3REGION.SKIDD_CUTSCENE:
                    UT_MAPPING[f"0/12/{loc_id}"] = loc_id + 51000000
                    UT_MAPPING[f"12/2/{loc_id}"] = loc_id + 51000000
                    UT_MAPPING[f"0/13/{loc_id}"] = loc_id + 51000000
                    UT_MAPPING[f"13/6/{loc_id}"] = loc_id + 51000000
                    tags.union({RAC3REGION.HOLOSTAR_STUDIOS, RAC3REGION.BLACKWATER_CITY})
                case RAC3REGION.NANOTECH:
                    section_id = LOCATION_NAME_TO_SECTION[location_name]
                    UT_MAPPING[f"0/{section_id}/{loc_id}"] = loc_id + 51000000
        check: list[RAC3ADDRESSDATA] = []
        for item in LOCATION_NAME_TO_ADDRESS[location_name]:
            check += [RAC3ADDRESSDATA(item)]
        return RAC3LOCATIONDATA(loc_id, planet_name, check, tags)


RAC3_LOCATION_DATA_TABLE: dict[str, RAC3LOCATIONDATA] = {name: RAC3LOCATIONDATA.construct(name) for name in
                                                         LOCATION_NAME_TO_ID.keys()}
LOCATION_FROM_AP_CODE: dict[int, str] = dict((kv[1].AP_CODE, kv[0]) for kv in RAC3_LOCATION_DATA_TABLE.items())

